<script setup>
const props = defineProps({
    modelValue: {
        type: String,
        required: true
    },
    options: {
        type: Array,
        required: true
    }
});

const emit = defineEmits(['update:modelValue']);

const updateFilter = (event) => {
    emit('update:modelValue', event.target.value);
};
</script>

<template>
    <div class="relative w-full md:w-1/3">
        <select
            :value="modelValue"
            @change="updateFilter"
            class="block appearance-none w-full bg-gray-700 border border-gray-600 text-white py-3 px-4 pr-8 rounded-full leading-tight focus:outline-none focus:bg-gray-600 focus:border-blue-500 transition-colors duration-200"
        >
            <option v-for="option in options" :key="option" :value="option">{{ option }}</option>
        </select>
        <div class="pointer-events-none absolute inset-y-0 right-0 flex items-center px-2 text-gray-400">
            <svg class="fill-current h-4 w-4" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20"><path d="M9.293 12.95l.707.707L15.657 8l-1.414-1.414L10 10.828 5.757 6.586 4.343 8z"/></svg>
        </div>
    </div>
</template>

